package cn.kduck.core.utils;

import java.util.Date;

public class SubObject {

    private String sonText;
    private Integer sonNum;
    private Date sonDate;
    private Boolean sonBool;
    private Double sonDecimalDouble;
    private Float sonDecimalFloat;

    public String getSonText() {
        return sonText;
    }

    public void setSonText(String sonText) {
        this.sonText = sonText;
    }

    public Integer getSonNum() {
        return sonNum;
    }

    public void setSonNum(Integer sonNum) {
        this.sonNum = sonNum;
    }

    public Date getSonDate() {
        return sonDate;
    }

    public void setSonDate(Date sonDate) {
        this.sonDate = sonDate;
    }

    public Boolean getSonBool() {
        return sonBool;
    }

    public void setSonBool(Boolean sonBool) {
        this.sonBool = sonBool;
    }

    public Double getSonDecimalDouble() {
        return sonDecimalDouble;
    }

    public void setSonDecimalDouble(Double sonDecimalDouble) {
        this.sonDecimalDouble = sonDecimalDouble;
    }

    public Float getSonDecimalFloat() {
        return sonDecimalFloat;
    }

    public void setSonDecimalFloat(Float sonDecimalFloat) {
        this.sonDecimalFloat = sonDecimalFloat;
    }

}
